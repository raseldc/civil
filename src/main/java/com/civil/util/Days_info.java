package com.civil.util;

/**
 *
 * @author Rasel
 */
public class Days_info {

    private int days;
    private int hours;
    private int miniutes;
    private int seconds;

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

    public int getHours() {
        return hours;
    }

    public void setHours(int hours) {
        this.hours = hours;
    }

    public int getMiniutes() {
        return miniutes;
    }

    public void setMiniutes(int miniutes) {
        this.miniutes = miniutes;
    }

    public int getSeconds() {
        return seconds;
    }

    public void setSeconds(int seconds) {
        this.seconds = seconds;
    }
}
